----- 2.5 -----
----- Напишете заявка, която извежда номер на модел, честота и размер на диска за
----- тези компютри с DVD 12x или 16x и цена по-малка от 2000 долара
SELECT model, speed, hd FROM pc WHERE price < 2000 AND (cd = '12x' OR cd = '16x');

----- 2.6 -----
----- Нека рейтингът на един лаптоп се определя по следната формула: 
----- честота на процесора + размер на RAM паметта + 10*размер на екрана.
----- Да се изведат кодовете, моделите и рейтингите на всички лаптопи. 
----- Резултатът да бъде подреден така, че първо да бъдат лаптопите с най-висок рейтинг,
----- а продукти с еднакъв рейтинг да бъдат подредени по код.
SELECT code, model, speed + ram + 10*screen AS rating
FROM laptop
ORDER BY rating DESC, code ASC;






