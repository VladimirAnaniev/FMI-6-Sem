----- 2.1 -----
----- Напишете заявка, която извежда номер на модел, честота на процесора (speed) 
----- и размер на диска (hd) за всички компютри с цена по-малка от 1200 долара. 
----- Задайте псевдоними за атрибутите честота и размер на диска, съответно MHz и GB
SELECT model, speed AS MHz, hd AS GB FROM pc WHERE price < 1200;

----- 2.2 -----
----- Напишете заявка, която извежда моделите и цените в евро на всички лаптопи.
----- Нека приемем, че в базата цените се съхраняват в долари, а курсът е 1.1 долара
----- за евро. Да се изведат първо най-евтините лаптопи
SELECT model, price/1.1 as EUR from laptop ORDER BY price ASC;

----- 2.3 -----
----- Напишете заявка, която извежда номер на модел, размер на паметта, размер на
----- екран за лаптопите, чиято цена е по-голяма от 1000 долара
SELECT model, hd, screen FROM laptop WHERE price > 1000;

